import java.util.Scanner;
public class memo4 {
	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		double a = scan.nextDouble();
		double b = scan.nextDouble();
		System.out.println(a +"+"+b+"="+(a+b));
		System.out.println(a +"*"+b+"="+(a*b));
		System.out.println(a +"-"+b+"="+(a-b));
		System.out.println(a +"/"+b+"="+(a/b));
		System.out.println(a +"%"+b+"="+(a%b));
	}

}
